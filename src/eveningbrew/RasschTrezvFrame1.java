    public Double CalcTrezv(Double num1, Double num2, Double num3, Double num4){
       Double res;
       Double r;
       Double k = 0.0;
       if(sex == true){
           r = 0.7;
       }
       else{
           r = 0.6;
       }
       if(num4<140){
           k = 1.0;
       }else if(num4>=140 && num4 < 160){
           k = 0.9;
       }else if(num4>=160 && num4<180){
           k = 0.8;
       }else if(num4>=180){
           k = 0.75;
       }
       res = (num2*k*(num3/100))/(num1*r);
       return res;
    }
