    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String weight;
        String volume;
        String strong;
        String height;
        Double weight_rev;
        Double volume_rev;
        Double strong_rev;
        Double height_rev;
        weight = jTextField1.getText();
        volume = jTextField2.getText();
        strong = jTextField3.getText();
        height = jTextField4.getText();
        weight_rev = Double.parseDouble(weight);
        volume_rev = Double.parseDouble(volume);
        strong_rev = Double.parseDouble(strong);
        height_rev = Double.parseDouble(height);
        Double num = CalcTrezv(weight_rev, volume_rev, strong_rev, height_rev);
        Double num_rev = new BigDecimal(num).setScale(2, RoundingMode.UP).doubleValue();
        String res_rev = num_rev.toString();
        jTextField5.setText(res_rev);
        if(num_rev<=0.6 && num_rev<1.5){
            jLabel9.setText("Отсутствие влияния алкоголя");
            jLabel10.setText("Выводится из организма через 30 минут");
        }else if (num_rev<1.5){
            jLabel9.setText("Легкая степень опьянения");
            jLabel10.setText("Выводится из организма через 3 часа  ");
        }else if(num_rev>=1.5 && num_rev<2.0){
            jLabel9.setText("Средняя степень опьянения");
            jLabel10.setText("Выводится из организма через 6 часов ");
        }else if(num_rev>=2.0 && num_rev<3.0){
            jLabel9.setText("Сильная степень опьянения");
            jLabel10.setText("Выводится из организма через 10 часов ");
        }else if(num_rev>=3.0 && num_rev<5.0){
            jLabel9.setText("Тяжелое отравление");
            jLabel10.setText("Выводится из организма через 14  часов ");
        }else if(num_rev>=5.0){
            jLabel9.setText("Смертельное отравление");
        }
        jPanel1.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

